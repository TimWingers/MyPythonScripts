#Находит дубликаты в списке и заносит их в словарь
def find_duplicates(lst):
    stnums = []  #создаем список дубликатов
    cnt = {}     #создаем словарь счетчик
    for el in lst: #перебираем основной список
        stnums.append(el[0])  #добавляем в него то что проверяем на дубли
    for i in stnums:  #перебираем список дубликатов
        if i in cnt:  #если ключ уже есть в словаре счетчике
            cnt[i] += 1 #прибавляем 1
        else:         #иначе
            cnt[i] = 1  #просто даем ключу значение в 1
    return cnt

