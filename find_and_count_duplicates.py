#Ниже функция которая заменяет дубликаты на нужное значение
def change_duplicates(lst, replacing_num):
    stnums = []   #создаем список дубликатов
    duplicates = find_duplicates(lst) #получаем словарь из 1й функции
    for k, v in duplicates.items():  #перебираем его
        if v > 1:    #если есть значения больше 1, то это дубль
            stnums = [k]  #добавляем ключ в список дубликатов
    for i1 in stnums:  #перебираем список дубликатов
        cnt = 0        #инициируем счетчик дублей для основного списка 
        for i2 in lst: #перебираем его
            if i1 == i2[0]:  #если есть совпадения двух списков
                cnt += 1  #увеличиваем счетчик на 1
                if cnt > 1:  #не трогаем 1ое совпадение, а только последующие
                    i2[0] = replacing_num  #заменяем 2ое, 3е и все последующие значения
    return lst
